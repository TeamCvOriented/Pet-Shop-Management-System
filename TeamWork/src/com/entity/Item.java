package com.entity;

public class Item {
  private int iId;
  private String iName;
  private int iInventory;
  private double iPrice;
  private String iIntroduce;
  private String iImage;
  
public int getiId() {
	return iId;
}
public void setiId(int iId) {
	this.iId = iId;
}
public String getiName() {
	return iName;
}
public void setiName(String iName) {
	this.iName = iName;
}
public int getiInventory() {
	return iInventory;
}
public void setiInventory(int iInventory) {
	this.iInventory = iInventory;
}
public double getiPrice() {
	return iPrice;
}
public void setiPrice(double iPrice) {
	this.iPrice = iPrice;
}
public String getiIntroduce() {
	return iIntroduce;
}
public void setiIntroduce(String iIntroduce) {
	this.iIntroduce = iIntroduce;
}
public String getiImage() {
	return iImage;
}
public void setiImage(String iImage) {
	this.iImage = iImage;
}  
}
